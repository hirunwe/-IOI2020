#include <bits/stdc++.h>
#define ll long long

using namespace std;

int main(){
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  cout.tie(NULL);
  int t;
  cin >> t;
  while(t--){
    ll a;
    cin >> a;
    if(a==1){cout << 1 << "\n";continue;}
    cout << (a-1)*2 << "\n";
  }
}

